#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int	main(void)
{
	int	first = 15;
	int	second = 2;
	ft_swap(&first, &second);
	printf("%d, %d", first, second);
}